class Person
{
	public Person(){}
}

class Teacher extends Person
{
	public Teacher()
	{
		System.out.println("I am a teacher.");
	}
}

class Student extends Person
{
	public Student()
	{
		System.out.println("I am a student.");
	}
}

class Athlete extends Person
{
	public Athlete()
	{
		System.out.println("I am a athlete");
	}
}